#Son las funciones que realizan las operaciones e imprimen el resultado.
def suma(n1,n2):
    result = n1 + n2
    print("El resultado de la suma es: {}".format(result))

def resta(n1,n2):
    result = n1 - n2
    print("El resultado de la resta es: {}".format(result))

def multi(n1,n2):
    result = n1 * n2
    print("El resultado de la multiplicación es: {}".format(result))

def div(n1,n2):
    result = n1 / n2
    print("El resultado de la división es: {}".format(result))

